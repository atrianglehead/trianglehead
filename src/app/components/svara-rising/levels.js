function makeDroneScaleLevel(id, notes) {
  const laneLabels = [...notes].reverse();

  return {
    id,
    title: `Locality ${id}`,
    interval: notes.join(' '),
    cueType: 'single-note',
    zoneCount: laneLabels.length,
    laneLabels,
    cueByLane: Object.fromEntries(laneLabels.map((note, lane) => [lane, NOTE_FREQS[note]])),
    helpText: `The drone holds Sa. Hear whether the cue is ${formatNoteList(laneLabels)}.`,
    description: 'Stage 3 scale locality.',
  };
}

